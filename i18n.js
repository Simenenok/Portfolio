  function getPreferredLanguage() {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && Object.keys(translations).includes(stored)) return stored;
    const navLang = navigator.language?.split("-")[0];
    if (navLang && Object.keys(translations).includes(navLang)) return navLang;
    return "no";
  }

  function setLanguage(lang) {
    if (!Object.keys(translations).includes(lang)) return;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.dataset.i18n;
      const translation = translations[lang][key];
      if (!translation) return;
      if (el.tagName.toLowerCase() === "title") {
        el.textContent = translation;
      } else {
        el.innerHTML = translation;
      }
    });

    const langSelect = document.getElementById("language-select");
    if (langSelect) langSelect.value = lang;
  }

  function init() {
    const langSelect = document.getElementById("language-select");
    if (langSelect) {
      langSelect.addEventListener("change", (event) => setLanguage(event.target.value));
    }

    setLanguage(getPreferredLanguage());
  }

  document.addEventListener("DOMContentLoaded", init);

  return {
    setLanguage,
    getPreferredLanguage,
  };
